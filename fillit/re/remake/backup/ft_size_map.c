#include "fillit.h"

int		ft_size_map(char *str)
{
	int i;
	int j;

	i = 0;
	j = 0;
	while (str[i])
	{
		if (str[i] == '#')
			j++;
		i++;
	}
	i = 2;
	while (i * i <= j)
		i++;
	return (i);
}
