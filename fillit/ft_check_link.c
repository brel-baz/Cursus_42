#include "libft.h"

int		ft_check_link(char *str)
{
	int		i;
	int		flag;

	i = 0;
	flag = 0;
	while (str[i])
	{
		if (str[i] == '#')
		{
			if ((str[i + 5] == '#') || (str[i - 5] == '#') ||
				(str[i + 1] == '#') || (str[i - 1] == '#'))
				flag = 1;
			else
				return (0);
		}
		i++;
	}
	return (flag);
}
