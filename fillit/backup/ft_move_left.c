#include "libft.h"

char	*ft_move_left(char*str)
{
	int		i;
	char	tmp;

	i = 0;
	while (str[i])
	{
		if (i - 1 >= 0)
		{
			if (str[i] == '#' && str[i - 1] == '.')
			{
				tmp = str[i - 1];
				str[i - 1] = str[i];
				str[i] = tmp;
			}
		}
		i++;
	}
	return (str);
}
